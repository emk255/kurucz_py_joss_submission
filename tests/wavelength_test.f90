      PROGRAM TEST_WAVELENGTH
      IMPLICIT REAL*8 (A-H,O-Z)
      REAL*8 W,AIRW,VACW
      W=500.D0
      AIRW=AIRVAC(W)
      VACW=VACAIR(AIRW)
      WRITE(6,100) W,AIRW,VACW,ABS(VACW-W)
  100 FORMAT('W=',F10.3,' nm  AIRVAC(W)=',F12.6,'  VACAIR(AIR)=',F12.6,
     1       '  roundtrip err=',1PE10.2)
      END

      FUNCTION AIRVAC(W)
      IMPLICIT REAL*8 (A-H,O-Z)
C     W IS AIR WAVELENGTH IN NM
C     WAVEN IS AIR WAVENUMBER WHICH IS USUALLY GOOD ENOUGH
C     MUST ITERATE FOR EXACT SOLUTION
      WAVEN=1.D7/W
      WNEW=W*(1.0000834213D0+
     1 2406030.D0/(1.30D10-WAVEN**2)+15997.D0/(3.89D9-WAVEN**2))
      WAVEN=1.D7/WNEW
      WNEW=W*(1.0000834213D0+
     1 2406030.D0/(1.30D10-WAVEN**2)+15997.D0/(3.89D9-WAVEN**2))
      WAVEN=1.D7/WNEW
      AIRVAC=W*(1.0000834213D0+
     1 2406030.D0/(1.30D10-WAVEN**2)+15997.D0/(3.89D9-WAVEN**2))
      RETURN
      END

      FUNCTION VACAIR(W)
      IMPLICIT REAL*8 (A-H,O-Z)
C     W IS VACUUM WAVELENGTH IN NM
      WAVEN=1.D7/W
      VACAIR=W/(1.0000834213D0+
     1 2406030.D0/(1.30D10-WAVEN**2)+15997.D0/(3.89D9-WAVEN**2))
      RETURN
      END

